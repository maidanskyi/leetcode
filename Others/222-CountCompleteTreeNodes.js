/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number}
 */
const countNodes = (root) => {

  let response = 0;
  const traverse = node => {
    if (!node) return;

    traverse(node.left);
    response += 1;
    traverse(node.right);

  }

  traverse(root);

  return response;
}

